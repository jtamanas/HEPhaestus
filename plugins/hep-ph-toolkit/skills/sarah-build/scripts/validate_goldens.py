def _parse_outputs(raw: str | None) -> list[str]:
    if not raw:
        return []
    return [o.strip() for o in raw.split(",") if o.strip()]


def _make_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Validate a SARAH golden-file tree: stage into Private-Models/, "
            "invoke wolframscript, grep for forbidden SARAH errors."
        ),
        prog="validate_goldens.py",
    )
    parser.add_argument(
        "--model",
        required=True,
        help="Spec-level model name (snake_case, e.g. 'singlet_doublet').",
    )
    parser.add_argument(
        "--goldens-dir",
        required=True,
        type=Path,
        help="Path to the goldens directory (e.g. tests/goldens/singlet_doublet/).",
    )
    parser.add_argument(
        "--outputs",
        default="",
        help=(
            "Comma-separated list of outputs to build and verify: "
            "'ufo', 'spheno', or 'ufo,spheno'.  Empty (default) runs "
            "Start + CheckModel only."
        ),
    )
    parser.add_argument(
        "--sarah-path",
        default=os.environ.get("SARAH_PATH", "/Users/yianni/SARAH/SARAH-4.15.3"),
        type=Path,
        help="Root of SARAH install (env: SARAH_PATH).",
    )
    parser.add_argument(
        "--wolframscript",
        default=os.environ.get("WOLFRAMSCRIPT", "/usr/local/bin/wolframscript"),
        type=Path,
        help="Path to wolframscript binary (env: WOLFRAMSCRIPT).",
    )
    parser.add_argument(
        "--keep-staged",
        action="store_true",
        help="Do NOT remove Private-Models/<Name>/ on exit (debug aid).",
    )
    parser.add_argument(
        "--timeout",
        type=float,
        default=None,
        help="Seconds before wolframscript is killed (default: no timeout).",
    )
    return parser


def main(argv: list[str] | None = None) -> int:
    args = _make_parser().parse_args(argv)
    outputs = _parse_outputs(args.outputs)

    try:
        result = validate(
            model=args.model,
            goldens_dir=args.goldens_dir,
            outputs=outputs,
            sarah_path=args.sarah_path,
            wolframscript=args.wolframscript,
            keep_staged=args.keep_staged,
            timeout=args.timeout,
        )
    except FileNotFoundError as exc:
        print(json.dumps({
            "status": "invalid",
            "code": "ENVIRONMENT_MISSING",
            "message": str(exc),
            "context": {},
        }))
        return 2
    except ValueError as exc:
        print(json.dumps({
            "status": "invalid",
            "code": "BAD_ARGUMENT",
            "message": str(exc),
            "context": {},
        }))
        return 2

    payload = result.as_json()
    print(json.dumps(payload))
    return 0 if result.ok else 1


if __name__ == "__main__":
    sys.exit(main())
