# ---------------------------------------------------------------------------
# Error-pattern grammar (design §10.3)
# ---------------------------------------------------------------------------

#: Fatal SARAH error signatures.  If any line of stdout+stderr matches one
#: of these regexes, the model is considered invalid.  The order here is
#: the order in which we report (first match wins in the final code
#: assignment).
#:
#: Cycle-2 hardening (T15a cycle-1 review oracle-gap fix): added the five
#: CheckModelFiles::*/Model::*/Set::partw patterns that T15a cycle-1 proved
#: SARAH emits when parameters.m / particles.m are incomplete (missing
#: mixing-matrix declarations, under-sized PDG lists, phases without decls).
#: Without these, SARAH silently produces UFO Particle() entries named
#: 'None' with Param.$Failed masses and zero DM vertices — invalid, but the
#: oracle returned VALID.  See cycle-1-review.md for the full diagnosis.
_FORBIDDEN_PATTERNS: tuple[tuple[str, re.Pattern[str]], ...] = (
    ("MODEL_FILE_MISSING",   re.compile(r"ModelFile::MissingModel")),
    ("MODEL_FILE_ABORTED",   re.compile(r"ModelFile::Aborted")),
    ("CHECKMODEL_ABORTED",   re.compile(r"CheckModel::.*Abort", re.IGNORECASE)),
    ("MATTERSECTOR_PARSE",   re.compile(r"MatterSector::parseError")),
    ("MISSING_PARAMETER",    re.compile(r"CheckModelFiles::MissingParameter")),
    ("MISSING_PARTICLE",     re.compile(r"CheckModelFiles::MissingParticle")),
    ("MISSING_OUTPUTNAME",   re.compile(r"CheckModelFiles::MissingOutputName(?:Parameter)?")),
    ("MODEL_NO_EC",          re.compile(r"Model::NoEC")),
    ("SET_PARTW",            re.compile(r"Set::partw")),
)


