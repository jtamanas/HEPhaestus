"""
validate_goldens.py — Golden-file oracle: stage + wolframscript drive + error grep.

This is the correctness gate for T15a/b/c goldens (plan-final.md §T19,
design-final.md §10.3). For each golden `.m` file tree, it:

1. Stages the tree into `$sarah_path/Private-Models/<SarahName>/` via
   :func:`stage.stage` (wipes stale .m and .mx caches — design §6.2).
2. Invokes ``wolframscript`` with the SARAH load + ``Start["<Name>"]`` +
   ``CheckModel[]`` + optional ``MakeUFO[]`` / ``MakeSPheno[]`` incantation.
3. Parses stdout + stderr for the forbidden error patterns.
4. Returns exit 0 on success, non-zero on failure with descriptive JSON.
5. Cleans up the staged tree on exit.

Forbidden error patterns (design §10.3):
    - ``ModelFile::MissingModel``  — Start[] couldn't find the model.
    - ``ModelFile::Aborted``       — Start[] aborted mid-load.
    - ``CheckModel::*Abort*``      — CheckModel[] aborted.
    - ``MatterSector::parseError`` — matter-sector block failed to parse.
    - Non-zero wolframscript exit status is also a failure.

For ``MakeUFO[]`` success: after the run, confirm
``$sarah_path/Output/<SarahName>/`` contains a ``UFO/`` subtree with
``particles.py`` (SARAH's standard UFO filename).

For ``MakeSPheno[]`` success: after the run, confirm
``$sarah_path/Output/<SarahName>/EWSB/SPheno/`` exists and contains
``SPheno.m.f90`` (SARAH's main SPheno generator output).

CLI:
    python3 scripts/validate_goldens.py \
        --model <name> \
        --goldens-dir <path-to-goldens/name/> \
        [--outputs ufo,spheno] \
        [--sarah-path <path>] \
        [--wolframscript <path>] \
        [--keep-staged]

Exit codes:
    0 — PASS (prints {"status": "valid"} JSON to stdout).
    1 — FAIL (prints {"status": "invalid", "code": ..., "context": {...}} JSON).
    2 — Usage / environment error (missing wolframscript, missing SARAH,
        missing goldens file).

The CLI is intentionally opt-in (not pytest): SARAH isn't in CI, and this
script is the equivalent of a pre-commit hook that developers run locally
before committing goldens regenerated by T15a/b/c.

Library API:
    from validate_goldens import validate, ValidationResult
    result = validate(
        model="singlet_doublet",
        goldens_dir=Path("tests/goldens/singlet_doublet"),
        outputs=["ufo", "spheno"],
        sarah_path=Path("/Users/yianni/SARAH/SARAH-4.15.3"),
        wolframscript=Path("/usr/local/bin/wolframscript"),
    )
    if result.ok:
        ...
"""

