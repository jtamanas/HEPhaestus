# [T3-1] Single-space SLHA replace — the original bug case
# ---------------------------------------------------------------------------

def test_single_space_slha_replace():
    """
    Card uses single space between index and value: '   1 0.000000e+00'.
    After _set_block_value(..., "DMSECTOR", (1,), 100.0) the index (1,) must
    hold 100.0, not be duplicated or left at 0.0.
    """
    card = (
        "Block MASS\n"
        "    1  1.25000e+02   # hh1\n"
        "\n"
        "Block DMSECTOR\n"
        "   1 0.000000e+00   # mchi (single-space)\n"
    )
    lines = _make_lines(card)
    result = _set_block_value(lines[:], "DMSECTOR", (1,), 100.0, "mchi")
    text = "".join(result)
    val = _get_value(text, "DMSECTOR", (1,))
    assert abs(val - 100.0) < 1e-9, f"[T3-1] DMSECTOR(1,) should be 100.0, got {val}"
    # No duplicate: exactly one indexed line in DMSECTOR
    entries = _block_entries(text, "DMSECTOR")
    assert len(entries) == 1, f"[T3-1] Expected 1 entry in DMSECTOR, found {len(entries)}: {entries}"


