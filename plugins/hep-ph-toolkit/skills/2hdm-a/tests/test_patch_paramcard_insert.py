# [T3-9] Full-patcher integration: DMSECTOR/PHASES/ZAMIX on fresh MG5 card
# ---------------------------------------------------------------------------

def test_full_patcher_fresh_card_dmsector_phases_zamix():
    """
    Run patch_paramcard() on a minimal fresh card that has ONLY Block MASS and
    Block SMINPUTS (DMSECTOR/PHASES/ZAMIX are absent — the original T1 finding).

    Asserts (per PLAN_FINAL T3 spec):
      1. re.search(r'^Block DMSECTOR\\s*$', text, re.M | re.I) is truthy.
      2. re.search(r'^Block PHASES\\s*$', text, re.M | re.I) is truthy.
      3. re.search(r'^Block ZAMIX\\s*$', text, re.M | re.I) is truthy.
      4. For each of the three blocks, at least one indexed line exists between
         the block header and the next ^Block /^Decay /EOF.
    """
    with tempfile.TemporaryDirectory() as tmp:
        run_dir = _make_run_dir(tmp, FRESH_CARD)
        patch_paramcard(run_dir)
        text = (run_dir / "Cards" / "param_card.dat").read_text()

    # 1-3: block headers present
    assert re.search(r"^Block DMSECTOR\s*$", text, re.M | re.I), \
        "[T3-9] Block DMSECTOR header absent after full patch on fresh card"
    assert re.search(r"^Block PHASES\s*$", text, re.M | re.I), \
        "[T3-9] Block PHASES header absent after full patch on fresh card"
    assert re.search(r"^Block ZAMIX\s*$", text, re.M | re.I), \
        "[T3-9] Block ZAMIX header absent after full patch on fresh card"

    # 4: each block has ≥1 indexed line beneath its header
    indexed_line_re = re.compile(r"^\s+\d+(\s+\d+)*\s+[-+0-9.eE]+")
    for block_name in ("DMSECTOR", "PHASES", "ZAMIX"):
        entries = _block_entries(text, block_name)
        assert len(entries) >= 1, \
            f"[T3-9] Block {block_name} has no indexed entries after full patch"

    # Spot-check: PHASES[1] must be 1.0 (the root-cause DM-coupling fix)
    phases_val = _get_value(text, "PHASES", (1,))
    assert abs(phases_val - 1.0) < 1e-9, \
        f"[T3-9] PHASES[1] = {phases_val}, expected 1.0 (PhasechiR fix)"

    # Spot-check: DMSECTOR[1] = 100.0 (default Mchi)
    dmsector_val = _get_value(text, "DMSECTOR", (1,))
    assert abs(dmsector_val - 100.0) < 1e-9, \
        f"[T3-9] DMSECTOR[1] = {dmsector_val}, expected 100.0 (default Mchi)"


