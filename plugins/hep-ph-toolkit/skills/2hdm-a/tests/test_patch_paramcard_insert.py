    lines = _make_lines(card)
    result = _set_block_value(lines[:], "MASS", (37,), 80.419, "MHm1 ~ Mw")
    text = "".join(result)
    val = _get_value(text, "MASS", (37,))
    assert abs(val - 80.419) < 1e-6, f"[T3-11] MASS(37,) should be 80.419 (Mw), got {val}"
