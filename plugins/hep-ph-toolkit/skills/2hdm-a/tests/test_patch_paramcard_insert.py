# [T3-3] Multi-index disambiguation: (1,1) vs (1,) vs (1,2)
# ---------------------------------------------------------------------------

def test_multi_index_disambiguation():
    """
    Block with rows (1,1), (1,2), (2,1).  Patching (1,1) → 0.5 must not
    touch (1,2) or (2,1).  Directly exercises the structural key lookup.
    """
    card = (
        "Block ZAMIX\n"
        "   1 1  0.000000e+00   # ZA(1,1)\n"
        "   1 2  0.000000e+00   # ZA(1,2)\n"
        "   2 1  0.000000e+00   # ZA(2,1)\n"
    )
    lines = _make_lines(card)
    result = _set_block_value(lines[:], "ZAMIX", (1, 1), 0.5)
    text = "".join(result)
    assert abs(_get_value(text, "ZAMIX", (1, 1)) - 0.5) < 1e-9, "[T3-3] (1,1) not updated"
    assert abs(_get_value(text, "ZAMIX", (1, 2)) - 0.0) < 1e-9, "[T3-3] (1,2) was corrupted"
    assert abs(_get_value(text, "ZAMIX", (2, 1)) - 0.0) < 1e-9, "[T3-3] (2,1) was corrupted"
    # Also verify single-index (1,) would not match any of the above
    result2 = _set_block_value(lines[:], "ZAMIX", (1,), 99.0)
    text2 = "".join(result2)
    # (1,1) and (1,2) must both be untouched (they are 2-index rows)
    assert abs(_get_value(text2, "ZAMIX", (1, 1)) - 0.0) < 1e-9, "[T3-3] (1,1) corrupted by (1,) patch"
    assert abs(_get_value(text2, "ZAMIX", (1, 2)) - 0.0) < 1e-9, "[T3-3] (1,2) corrupted by (1,) patch"


