def _make_lines(text: str):
    """Return a list of lines (with line endings) from a card string."""
    return text.splitlines(keepends=True)


def _make_run_dir(tmp_dir: str, card_text: str) -> Path:
    """Write card_text into <tmp_dir>/Cards/param_card.dat; return Path(tmp_dir)."""
    run_dir = Path(tmp_dir)
    cards = run_dir / "Cards"
    cards.mkdir(parents=True, exist_ok=True)
    (cards / "param_card.dat").write_text(card_text)
    return run_dir


def _block_entries(text: str, block: str):
    """
    Return all indexed lines (leading integers + float value) inside the named
    block as a list of (tuple_of_ints, float) pairs.
    Stops at the next Block/Decay header or EOF.
    """
    entries = []
    in_block = False
    for line in text.splitlines():
        stripped = line.strip()
        if stripped.lower().startswith("block "):
            in_block = stripped.split()[1].upper() == block.upper()
            continue
        if stripped.lower().startswith("decay"):
            in_block = False
            continue
        if not in_block or not stripped or stripped.startswith("#"):
            continue
        parts = stripped.split()
        idx = []
        for p in parts:
            try:
                idx.append(int(p))
            except ValueError:
                break
        if idx and len(parts) > len(idx):
            try:
                val = float(parts[len(idx)])
                entries.append((tuple(idx), val))
            except ValueError:
                pass
    return entries


def _get_value(text: str, block: str, index: tuple) -> float:
    """Return the float value for the given block/index; raise AssertionError if absent."""
    for idx_tuple, val in _block_entries(text, block):
        if idx_tuple == index:
            return val
    raise AssertionError(f"Block {block} index {index} not found")


