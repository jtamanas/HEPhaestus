"""
test_patch_paramcard.py — unit tests for the 2HDM+a param_card patcher.

Covers:
  - PHASES[1] set to 1.0 (the root-cause DM-coupling fix)
  - IMPHASES[1] set to 0.0
  - Tightened regex: single-index _set_block_value must NOT clobber a 2-index row
    whose first element happens to match (e.g. "1 2 val" must survive when patching
    index (1,))
  - Multi-index _set_block_value (UDLMIX 1 1 → 1.0) does NOT touch row "1 2"
  - Idempotency: two consecutive patcher calls produce identical output
  - Untouched blocks: SMINPUTS alpha_em entry is bytewise unchanged
"""

