# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _write_card(tmp_dir: Path, text: str) -> Path:
    """Write a Cards/param_card.dat into tmp_dir and return its path."""
    cards = tmp_dir / "Cards"
    cards.mkdir(parents=True, exist_ok=True)
    card_path = cards / "param_card.dat"
    card_path.write_text(text)
    return card_path


def _get_block_value(text: str, block: str, index: tuple) -> float:
    """
    Minimal inline parser: find the first line inside BLOCK <block> whose
    leading integer tokens match *index exactly* and return its float value.
    Raises AssertionError if not found.
    """
    in_block = False
    idx_ints = list(index)
    for line in text.splitlines():
        stripped = line.strip()
        if stripped.lower().startswith("block "):
            in_block = stripped.split()[1].upper() == block.upper()
            continue
        if stripped.lower().startswith("decay"):
            in_block = False
            continue
        if not in_block or not stripped or stripped.startswith("#"):
            continue
        parts = stripped.split()
        leading = []
        for p in parts:
            try:
                leading.append(int(p))
            except ValueError:
                break
        if leading == idx_ints and len(parts) > len(idx_ints):
            return float(parts[len(idx_ints)])
    raise AssertionError(f"Block {block} index {index} not found in card text")


# ---------------------------------------------------------------------------
# Tests
# ---------------------------------------------------------------------------

def test_phases_set_to_one():
    """PHASES[1] must be 1.0 after patching (root-cause fix)."""
    with tempfile.TemporaryDirectory() as tmp:
        run_dir = Path(tmp)
        _write_card(run_dir, PARAM_CARD_FIXTURE)
        patch_paramcard(run_dir)
        result = (run_dir / "Cards" / "param_card.dat").read_text()
        assert _get_block_value(result, "PHASES", (1,)) == 1.0, (
            "PHASES[1] should be 1.0 to restore DM couplings"
        )


def test_imphases_set_to_zero():
    """IMPHASES[1] must be 0.0 after patching (imaginary part stays zero)."""
    with tempfile.TemporaryDirectory() as tmp:
        run_dir = Path(tmp)
        _write_card(run_dir, PARAM_CARD_FIXTURE)
        patch_paramcard(run_dir)
        result = (run_dir / "Cards" / "param_card.dat").read_text()
        assert _get_block_value(result, "IMPHASES", (1,)) == 0.0, (
            "IMPHASES[1] should be 0.0 (pure-real phase)"
        )


def test_single_index_does_not_clobber_two_index_row():
    """
    _set_block_value(lines, "UDLMIX", (1,), 99.0) must NOT overwrite the
    "1 2  0.0" row — the tightened regex (?!\\d) prevents this collision.
    """
    lines = PARAM_CARD_FIXTURE.splitlines(keepends=True)
    # Attempt to set a single-index (1,) entry in UDLMIX
    modified = _set_block_value(lines[:], "UDLMIX", (1,), 99.0, "probe")
    text = "".join(modified)
    # The 1 2 row in UDLMIX must be untouched
    val_1_2 = _get_block_value(text, "UDLMIX", (1, 2))
    assert val_1_2 == 0.0, (
        f"UDLMIX (1,2) was corrupted by single-index patch: got {val_1_2}"
    )


def test_two_index_patch_does_not_touch_adjacent_row():
    """
    _set_block_value(lines, "UDLMIX", (1, 1), 1.0) must NOT modify row (1, 2).
    """
    lines = PARAM_CARD_FIXTURE.splitlines(keepends=True)
    modified = _set_block_value(lines[:], "UDLMIX", (1, 1), 1.0, "identity")
    text = "".join(modified)
    val_1_2 = _get_block_value(text, "UDLMIX", (1, 2))
    assert val_1_2 == 0.0, (
        f"UDLMIX (1,2) corrupted when patching (1,1): got {val_1_2}"
    )


def test_idempotency():
    """Running the patcher twice produces identical output to a single run."""
    with tempfile.TemporaryDirectory() as tmp:
        run_dir = Path(tmp)
        _write_card(run_dir, PARAM_CARD_FIXTURE)
        patch_paramcard(run_dir)
        after_one = (run_dir / "Cards" / "param_card.dat").read_text()
        patch_paramcard(run_dir)
        after_two = (run_dir / "Cards" / "param_card.dat").read_text()
        assert after_one == after_two, (
            "patch_paramcard is not idempotent: second run changed the card"
        )


def test_untouched_block_unchanged():
    """
    SMINPUTS block must be bytewise unchanged after patching.
    Specifically, the alpha_em inverse row (1  1.31667e+02) must survive intact.
    """
    with tempfile.TemporaryDirectory() as tmp:
        run_dir = Path(tmp)
        _write_card(run_dir, PARAM_CARD_FIXTURE)
        patch_paramcard(run_dir)
        result = (run_dir / "Cards" / "param_card.dat").read_text()
        aew_val = _get_block_value(result, "SMINPUTS", (1,))
        assert abs(aew_val - 1.31667e+02) < 1e-3, (
            f"SMINPUTS[1] (alpha_em inverse) was modified: got {aew_val}"
        )
