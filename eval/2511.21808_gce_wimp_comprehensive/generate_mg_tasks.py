"""
Conditional MadGraph task YAML generator for arXiv:2511.21808.

Writes eval/tasks/tier3_mg_paper4.generated.yaml only when:
  1. madgraph/zprime_BminusL/model.sha256 exists AND
  2. Its content matches EXPECTED_SHA (set below when UFO is vendored).

If either condition fails, emits a message and exits 0 cleanly.
The harness loader (eval/harness/loader.py) picks up YAML files via
glob eval/tasks/*.yaml; if the generated file doesn't exist, the
loader silently sees no B-L MG task (Plan C acceptable end state).

Plan C status: EXPECTED_SHA = "UNSET" → generator always skips.
To activate Plan A/B: vendor the UFO, compute SHA, fill EXPECTED_SHA.
"""

import sys
from pathlib import Path
import hashlib

# -------------------------------------------------------------------
# Configuration
# -------------------------------------------------------------------

UFO_SHA_PATH = Path(__file__).parent / "madgraph" / "zprime_BminusL" / "model.sha256"

# Fill with actual SHA256 of UFO tarball when vendored.
# Example: sha256sum zprime_BminusL_UFO.tar.gz → output goes here.
EXPECTED_SHA = "UNSET"

OUT = Path(__file__).resolve().parents[2] / "eval" / "tasks" / "tier3_mg_paper4.generated.yaml"

# -------------------------------------------------------------------
# MG task YAML block (written only on SHA match)
# -------------------------------------------------------------------

MG_TASK_YAML_BLOCK = """\
# Generated by eval/2511.21808_gce_wimp_comprehensive/generate_mg_tasks.py
# Only present when U1B-L UFO is vendored with matching SHA.

- id: t1_p4_BminusL_proc_card
  tier: 1
  model: zprime-BminusL
  prompt: >
    Using mg5_batch.generate_proc_card(), write a proc_card for DM pair
    annihilation chi chi~ > b b~ via a B-L Z' mediator. UFO model: U1B-L
    (version/SHA pinned in eval/2511.21808_gce_wimp_comprehensive/madgraph/README.md).
    Channels: b b~, tau+ tau-, c c~. Report under key "proc_card".
  reference_fn: p4_proc_card_zprime_BminusL
  reference_args: {}
  graders:
    - type: file_contains
      key: proc_card
      pattern: "import model U1B-L"
    - type: file_contains
      key: proc_card
      pattern: "generate.*chi.*>.*b.*b~"
  tags: [paper-4, zprime, BminusL, setup, madgraph]

- id: t3_p4_BminusL_mg_vs_closed
  tier: 3
  model: zprime-BminusL
  prompt: >
    Run MG5_aMC on the B-L UFO for chi chi~ > b b~ at sqrt(s) = 2*m_DM + 10 GeV.
    m_DM=50, m_Zp=200, g_chi=0.1, g_Zp=0.01 (Dirac). Extract sigma_v via
    v -> 0 extrapolation. Report ratio sigma_v(MG) / sigma_v(closed) under
    key "ratio_mg_over_closed".
  reference_fn: p4_bminusl_mg_vs_closed
  reference_args:
    m_DM: 50.0
    m_Zp: 200.0
    g_chi: 0.1
    g_Zp: 0.01
    dm_type: "Dirac"
  graders:
    - type: numeric
      key: ratio_mg_over_closed
      tolerance: 0.15
      expected: 1.0
  tags: [paper-4, zprime, BminusL, madgraph, cross-check]
"""


def main():
    # Check SHA file exists
    if not UFO_SHA_PATH.exists():
        print(f"MG UFO SHA file absent ({UFO_SHA_PATH}) — skipping MG task generation.")
        sys.exit(0)

    actual_sha = UFO_SHA_PATH.read_text().strip()

    if EXPECTED_SHA == "UNSET":
        print(f"EXPECTED_SHA is UNSET (Plan C: UFO not vendored) — skipping MG task generation.")
        sys.exit(0)

    if actual_sha != EXPECTED_SHA:
        print(
            f"MG UFO SHA mismatch "
            f"(actual={actual_sha[:12]}..., expected={EXPECTED_SHA[:12]}...) — skipping."
        )
        sys.exit(0)

    # All checks passed: write the YAML
    OUT.parent.mkdir(parents=True, exist_ok=True)
    OUT.write_text(MG_TASK_YAML_BLOCK)
    print(f"Wrote {OUT}")


if __name__ == "__main__":
    main()
